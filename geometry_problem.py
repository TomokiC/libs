def judge_square(p1, p2, p3, p4):
    """4点が正方形かどうかを判断"""
    d12 = (p2[0] - p1[0]) ** 2 + (p2[1] - p1[1]) ** 2
    d23 = (p3[0] - p2[0]) ** 2 + (p3[1] - p2[1]) ** 2
    d34 = (p4[0] - p3[0]) ** 2 + (p4[1] - p3[1]) ** 2
    d41 = (p1[0] - p4[0]) ** 2 + (p1[1] - p4[1]) ** 2
    d13 = (p1[0] - p3[0]) ** 2 + (p1[1] - p3[1]) ** 2
    d24 = (p2[0] - p4[0]) ** 2 + (p2[1] - p4[1]) ** 2
    d = [d12, d23, d34, d41, d13, d24]
    d.sort()
    if d[0] == d[1] == d[2] == d[3] and 2 * d[0] == d[4] == d[5]:
        return True
    else:
        return False
